package com.hardcore.accounting.model;

public class greeting {
    private final Long id;
    private String name;

    public greeting(Long id, String name) {
        this.id = id;
        this.name = name;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
